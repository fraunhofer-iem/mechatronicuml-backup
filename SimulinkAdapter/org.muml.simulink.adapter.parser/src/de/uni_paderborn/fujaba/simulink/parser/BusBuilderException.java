package de.uni_paderborn.fujaba.simulink.parser;

public class BusBuilderException extends Exception {

	private static final long serialVersionUID = 2871643304485161305L;

	public BusBuilderException() {
	}

	public BusBuilderException(String message) {
		super(message);
	}

	public BusBuilderException(Throwable cause) {
		super(cause);
	}

	public BusBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

}
