package org.muml.simulink.adapter.parser;

public class StateflowBuilderException extends SimulinkBuilderException {

	private static final long serialVersionUID = 8432985340876693553L;

	public StateflowBuilderException() {
	}

	public StateflowBuilderException(String message) {
		super(message);
	}

	public StateflowBuilderException(Throwable cause) {
		super(cause);
	}

	public StateflowBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

}
