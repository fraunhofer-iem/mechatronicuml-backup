package org.muml.simulink.adapter.m2t.graphviz;

public class GraphVizReaderException extends Exception {

	private static final long serialVersionUID = 7936407677117242380L;

	public GraphVizReaderException() {
	}

	public GraphVizReaderException(String message) {
		super(message);
	}

	public GraphVizReaderException(Throwable cause) {
		super(cause);
	}

	public GraphVizReaderException(String message, Throwable cause) {
		super(message, cause);
	}

}
