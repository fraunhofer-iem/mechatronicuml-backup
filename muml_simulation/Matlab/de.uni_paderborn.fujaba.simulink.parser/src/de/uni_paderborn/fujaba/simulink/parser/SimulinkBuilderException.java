package de.uni_paderborn.fujaba.simulink.parser;

public class SimulinkBuilderException extends Exception {

	private static final long serialVersionUID = -4650778309713816459L;

	public SimulinkBuilderException() {
	}

	public SimulinkBuilderException(String message) {
		super(message);
	}

	public SimulinkBuilderException(Throwable cause) {
		super(cause);
	}

	public SimulinkBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

}
